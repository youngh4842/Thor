package thor.vo;

public class RentVO {
	private int r_num;
	private String r_ref_Mid;
	private String r_name;
	private String r_startDays;
	private String r_endDays;
	private Integer r_status;
	private String r_carUrl;
	
	public int getR_num() {
		return r_num;
	}
	public void setR_num(int r_num) {
		this.r_num = r_num;
	}
	public String getR_ref_Mid() {
		return r_ref_Mid;
	}
	public void setR_ref_Mid(String r_ref_Mid) {
		this.r_ref_Mid = r_ref_Mid;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	public String getR_startDays() {
		return r_startDays;
	}
	public void setR_startDays(String r_startDays) {
		this.r_startDays = r_startDays;
	}
	public String getR_endDays() {
		return r_endDays;
	}
	public void setR_endDays(String r_endDays) {
		this.r_endDays = r_endDays;
	}
	public Integer getR_status() {
		return r_status;
	}
	public void setR_status(Integer r_status) {
		this.r_status = r_status;
	}
	public String getR_carUrl() {
		return r_carUrl;
	}
	public void setR_carUrl(String r_carUrl) {
		this.r_carUrl = r_carUrl;
	}
}

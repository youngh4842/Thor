package thor.vo;

public class QuestionVO {
	private int q_num;
	private String q_title;
	private String q_ref_Mid;
	private String q_content;
	private String q_days;
	
	public int getQ_num() {
		return q_num;
	}
	public void setQ_num(int q_num) {
		this.q_num = q_num;
	}
	public String getQ_title() {
		return q_title;
	}
	public void setQ_title(String q_title) {
		this.q_title = q_title;
	}
	public String getQ_ref_Mid() {
		return q_ref_Mid;
	}
	public void setQ_ref_Mid(String q_ref_Mid) {
		this.q_ref_Mid = q_ref_Mid;
	}
	public String getQ_content() {
		return q_content;
	}
	public void setQ_content(String q_content) {
		this.q_content = q_content;
	}
	public String getQ_days() {
		return q_days;
	}
	public void setQ_days(String q_days) {
		this.q_days = q_days;
	}
}

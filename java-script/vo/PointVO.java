package thor.vo;

public class PointVO {
	private int p_num;
	private String p_ref_Mid;
	private String p_days;
	private int p_changep;
	private int p_remainp;
	private String p_content;
	
	public int getP_num() {
		return p_num;
	}
	public void setP_num(int p_num) {
		this.p_num = p_num;
	}
	public String getP_ref_Mid() {
		return p_ref_Mid;
	}
	public void setP_ref_Mid(String p_ref_Mid) {
		this.p_ref_Mid = p_ref_Mid;
	}
	public String getP_days() {
		return p_days;
	}
	public void setP_days(String p_days) {
		this.p_days = p_days;
	}
	public int getP_changep() {
		return p_changep;
	}
	public void setP_changep(int p_changep) {
		this.p_changep = p_changep;
	}
	public int getP_remainp() {
		return p_remainp;
	}
	public void setP_remainp(int p_remainp) {
		this.p_remainp = p_remainp;
	}
	public String getP_content() {
		return p_content;
	}
	public void setP_content(String p_content) {
		this.p_content = p_content;
	}
}

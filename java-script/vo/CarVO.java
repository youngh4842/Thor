package thor.vo;

public class CarVO {
	private String c_name;
	private String c_company;
	private int c_cost;
	private int c_fuel;
	private String c_url;
	
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_company() {
		return c_company;
	}
	public void setC_company(String c_company) {
		this.c_company = c_company;
	}
	public int getC_cost() {
		return c_cost;
	}
	public void setC_cost(int c_cost) {
		this.c_cost = c_cost;
	}
	public int getC_fuel() {
		return c_fuel;
	}
	public void setC_fuel(int c_fuel) {
		this.c_fuel = c_fuel;
	}
	public String getC_url() {
		return c_url;
	}
	public void setC_url(String c_url) {
		this.c_url = c_url;
	}
}
